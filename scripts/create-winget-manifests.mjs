import { createHash } from "node:crypto";
import {
  existsSync,
  mkdirSync,
  readFileSync,
  readdirSync,
  statSync,
  writeFileSync,
} from "node:fs";
import { basename, resolve } from "node:path";
import process from "node:process";
import { pathToFileURL } from "node:url";
import yaml from "js-yaml";

const manifestVersion = "1.12.0";
const repositoryUrl =
  "https://github.com/apotenza92/facebook-messenger-desktop";

const channels = {
  stable: {
    identifier: "apotenza92.FacebookMessengerDesktop",
    packageName: "Messenger",
    moniker: "messenger-desktop",
    artifactPrefix: "Messenger",
  },
  beta: {
    identifier: "apotenza92.FacebookMessengerDesktopBeta",
    packageName: "Messenger Beta",
    moniker: "messenger-desktop-beta",
    artifactPrefix: "Messenger-Beta",
  },
};

function fail(message) {
  throw new Error(message);
}

function option(name) {
  const index = process.argv.indexOf(name);
  return index === -1 ? null : process.argv[index + 1];
}

function requireOption(name) {
  const value = option(name);
  if (!value || value.startsWith("--")) fail(`${name} is required`);
  return value;
}

function assertVersion(version, channel) {
  const expected =
    channel === "beta"
      ? /^\d+\.\d+\.\d+-beta\.[1-9]\d*$/
      : /^\d+\.\d+\.\d+$/;
  if (!expected.test(version)) {
    fail(`Unsupported ${channel} WinGet package version ${version}`);
  }
}

function assertReleaseDate(releaseDate) {
  if (!/^\d{4}-\d{2}-\d{2}$/.test(releaseDate)) {
    fail(`Release date must use YYYY-MM-DD; received ${releaseDate}`);
  }
  const parsed = new Date(`${releaseDate}T00:00:00Z`);
  if (Number.isNaN(parsed.valueOf()) || parsed.toISOString().slice(0, 10) !== releaseDate) {
    fail(`Release date is invalid; received ${releaseDate}`);
  }
}

function assertHttpsReleaseUrl(url, expectedName, version) {
  let parsed;
  try {
    parsed = new URL(url);
  } catch {
    fail(`Invalid installer URL ${url}`);
  }
  if (parsed.protocol !== "https:") fail(`Installer URL must use HTTPS: ${url}`);
  if (parsed.hostname.toLowerCase() !== "github.com") {
    fail(`Installer URL must use github.com: ${url}`);
  }
  const expectedPath =
    `/apotenza92/facebook-messenger-desktop/releases/download/v${version}/${expectedName}`.toLowerCase();
  if (decodeURIComponent(parsed.pathname).toLowerCase() !== expectedPath) {
    fail(`Installer URL does not match the immutable release asset ${expectedName}`);
  }
}

function sha256(filePath) {
  return createHash("sha256").update(readFileSync(filePath)).digest("hex").toUpperCase();
}

function documentHeader(schemaName) {
  return [
    "# Generated by scripts/create-winget-manifests.mjs",
    `# yaml-language-server: $schema=https://aka.ms/winget-manifest.${schemaName}.${manifestVersion}.schema.json`,
    "",
  ].join("\n");
}

function writeDocument(outputDirectory, fileName, schemaName, document) {
  writeFileSync(
    resolve(outputDirectory, fileName),
    `${documentHeader(schemaName)}${yaml.dump(document, {
      lineWidth: -1,
      noRefs: true,
      sortKeys: false,
    })}`,
    { flag: "wx" },
  );
}

export function createWingetManifests({
  channel,
  version,
  releaseDate,
  x64Installer,
  x64Url,
  arm64Installer,
  arm64Url,
  outputDirectory,
}) {
  const channelConfig = channels[channel];
  if (!channelConfig) fail(`Unsupported WinGet channel ${channel}`);
  assertVersion(version, channel);
  assertReleaseDate(releaseDate);

  const installers = [
    { architecture: "x64", file: resolve(x64Installer), url: x64Url },
    { architecture: "arm64", file: resolve(arm64Installer), url: arm64Url },
  ].map(({ architecture, file, url }) => {
    const expectedName = `${channelConfig.artifactPrefix}-windows-${architecture}-setup.exe`;
    if (!existsSync(file) || !statSync(file).isFile()) {
      fail(`Missing ${architecture} installer ${file}`);
    }
    if (basename(file) !== expectedName) {
      fail(`${architecture} installer must be named ${expectedName}`);
    }
    assertHttpsReleaseUrl(url, expectedName, version);
    return {
      Architecture: architecture,
      InstallerUrl: url,
      InstallerSha256: sha256(file),
    };
  });

  const output = resolve(outputDirectory);
  mkdirSync(output, { recursive: true });
  const existing = readdirSync(output);
  if (existing.length > 0) {
    fail(`WinGet output directory must be empty; found ${existing.join(", ")}`);
  }

  const prefix = channelConfig.identifier;
  writeDocument(output, `${prefix}.yaml`, "version", {
    PackageIdentifier: channelConfig.identifier,
    PackageVersion: version,
    DefaultLocale: "en-US",
    ManifestType: "version",
    ManifestVersion: manifestVersion,
  });
  writeDocument(output, `${prefix}.installer.yaml`, "installer", {
    PackageIdentifier: channelConfig.identifier,
    PackageVersion: version,
    InstallerType: "nullsoft",
    Scope: "user",
    InstallModes: ["interactive", "silent", "silentWithProgress"],
    UpgradeBehavior: "install",
    ReleaseDate: releaseDate,
    Installers: installers,
    ManifestType: "installer",
    ManifestVersion: manifestVersion,
  });
  writeDocument(output, `${prefix}.locale.en-US.yaml`, "defaultLocale", {
    PackageIdentifier: channelConfig.identifier,
    PackageVersion: version,
    PackageLocale: "en-US",
    Publisher: "Alex Potenza",
    PublisherUrl: "https://github.com/apotenza92",
    PublisherSupportUrl: `${repositoryUrl}/issues`,
    PackageName: channelConfig.packageName,
    PackageUrl: repositoryUrl,
    License: "MIT",
    LicenseUrl: `${repositoryUrl}/blob/main/LICENSE`,
    Copyright: "Copyright (c) 2026 Alex Potenza",
    ShortDescription:
      "An unofficial, self-contained desktop app for Facebook Messenger.",
    Moniker: channelConfig.moniker,
    Tags: ["chat", "facebook", "messaging", "messenger"],
    ReleaseNotesUrl: `${repositoryUrl}/releases/tag/v${version}`,
    ManifestType: "defaultLocale",
    ManifestVersion: manifestVersion,
  });

  return {
    identifier: channelConfig.identifier,
    files: readdirSync(output).sort(),
  };
}

const invoked =
  process.argv[1] && pathToFileURL(resolve(process.argv[1])).href === import.meta.url;

if (invoked) {
  const result = createWingetManifests({
    channel: requireOption("--channel"),
    version: requireOption("--version"),
    releaseDate: requireOption("--release-date"),
    x64Installer: requireOption("--x64-installer"),
    x64Url: requireOption("--x64-url"),
    arm64Installer: requireOption("--arm64-installer"),
    arm64Url: requireOption("--arm64-url"),
    outputDirectory: requireOption("--output-dir"),
  });
  console.log(
    `Generated ${result.files.length} WinGet manifests for ${result.identifier}`,
  );
}
